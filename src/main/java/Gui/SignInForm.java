    private String asignarCorreo(String text) {
         Pattern pattern = Pattern
                .compile("^[^@]+@[^@]+\\.[a-zA-Z]{2,}$",Pattern.CASE_INSENSITIVE);
 
        Matcher m = pattern.matcher(text);
        if(m.find()){
            return text;
        }else{
            return "";
        }
    }

    private String asignarDni(String text) {
        Pattern pattern = Pattern
                .compile("[0-9]{7,8}[A-Z a-z]",Pattern.CASE_INSENSITIVE);
        Matcher m = pattern.matcher(text);
        if(m.find()){
            return text;
        }else{
            return "";
        }        
    }
