    private void ConfirmarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmarDatosActionPerformed
        // TODO add your handling code here:
        String nombreCompleto = nameText.getText() +" "+ apellidosText1.getText();
        int telefono = Integer.valueOf(telefonoText.getText().replaceAll(" ", ""));
        String Correo = asignarCorreo(correoText.getText());
        String Dni = asignarDni(dniText.getText());
        System.out.println(Dni);
        if(Correo.equals("") || Dni.equals("") || Math.floor(Math.log10(Math.abs(telefono)) + 1) != 9){
                new DatosIncorrectos().setVisible(true);
                return;
        }else{
            for(int i = 0; i < ControladorServicio.getClientes().size(); i++){
            if(Correo.equalsIgnoreCase(ControladorServicio.getClientes().get(i).getCorreo())
                    || Dni.equalsIgnoreCase(ControladorServicio.getClientes().get(i).getDni())){
                new correoDniRepetidos().setVisible(true);
                return;
            }
                    
        }            
        String Direccion = direccionText.getText();
        String Contraseña = passwordText.getText();
        ControladorServicio.crearCliente((String) nombreCompleto, telefono, Correo,(String) Dni, Direccion, Contraseña); 
        ControladorServicio.setClienteActual(Dni);
        AppConcesionarioUI interfaz = new AppConcesionarioUI();
        interfaz.LoggedIn();
        interfaz.setVisible(true);
        this.setVisible(false);  
        }
        
    }//GEN-LAST:event_ConfirmarDatosActionPerformed

